#pragma once
#include "Object.h"

class ScreenCanvasObject: public Object {
public:
	float width;
	float height;
	
	
	ScreenCanvasObject() {
		name = "Plane";
		width = 1;
		height = 1;
	}
	
	virtual void parameterize(xmlgui::SimpleGui &gui) {
		gui.addFloatField("width", width);
		gui.addFloatField("height", height);
	}
	
	void draw() {
		ofRect(-width/2, -height/2, width, height);
	}
};






